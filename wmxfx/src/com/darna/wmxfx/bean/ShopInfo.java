package com.darna.wmxfx.bean;

public class ShopInfo {
	String shop_id, thumb, shop_name, address, notice, average_cost, businesshour, activitydesc,  
	distance, deliverycost;
	float average_point;

	public String getShop_id() {
		return shop_id;
	}

	public void setShop_id(String shop_id) {
		this.shop_id = shop_id;
	}

	public String getThumb() {
		return thumb;
	}

	public void setThumb(String thumb) {
		this.thumb = thumb;
	}

	public String getShop_name() {
		return shop_name;
	}

	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getNotice() {
		return notice;
	}

	public void setNotice(String notice) {
		this.notice = notice;
	}

	public String getAverage_cost() {
		return average_cost;
	}

	public void setAverage_cost(String average_cost) {
		this.average_cost = average_cost;
	}

	public String getBusinesshour() {
		return businesshour;
	}

	public void setBusinesshour(String businesshour) {
		this.businesshour = businesshour;
	}

	public String getActivitydesc() {
		return activitydesc;
	}

	public void setActivitydesc(String activitydesc) {
		this.activitydesc = activitydesc;
	}

	public String getDistance() {
		return distance;
	}

	public void setDistance(String distance) {
		this.distance = distance;
	}

	public String getDeliverycost() {
		return deliverycost;
	}

	public void setDeliverycost(String deliverycost) {
		this.deliverycost = deliverycost;
	}

	public float getAverage_point() {
		return average_point;
	}

	public void setAverage_point(float average_point) {
		this.average_point = average_point;
	}

}

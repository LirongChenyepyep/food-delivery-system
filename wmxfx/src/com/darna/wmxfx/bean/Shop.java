package com.darna.wmxfx.bean;

public class Shop {
	String shop_name, shop_id, shop_thumb, distance, deli_time, notice;
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	public String getShop_id() {
		return shop_id;
	}
	public void setShop_id(String shop_id) {
		this.shop_id = shop_id;
	}
	public String getShop_thumb() {
		return shop_thumb;
	}
	public void setShop_thumb(String shop_thumb) {
		this.shop_thumb = shop_thumb;
	}
	public String getDistance() {
		return distance;
	}
	public void setDistance(String distance) {
		this.distance = distance;
	}
	public String getDeli_time() {
		return deli_time;
	}
	public void setDeli_time(String deli_time) {
		this.deli_time = deli_time;
	}
	public String getNotice() {
		return notice;
	}
	public void setNotice(String notice) {
		this.notice = notice;
	}
}
